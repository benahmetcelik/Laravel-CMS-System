function t($key = null, $replace = [], $locale = null)
{
    if (is_null($key)) {
        return $key;
    }

    return trans($key, $replace, $locale);
}


function emptyDir($dir) {

    if (is_dir($dir)) {
        $scn = scandir($dir);
        foreach ($scn as $files) {
            if ($files !== '.') {
                if ($files !== '..') {
                    if (!is_dir($dir . '/' . $files)) {
                        if (unlink($dir . '/' . $files)){
                            return true;
                        }
                    } else {
                        emptyDir($dir . '/' . $files);
                        if ( rmdir($dir . '/' . $files)){
                            return true;

                        }
                    }
                }
            }
        }
    }
}

function deleteLangDir($dir) {
    $dir ="../resources/lang/".$dir;
    foreach(glob($dir . '/' . '*') as $file) {
        if(is_dir($file)){
            deleteLangDir($file);
        } else {
            unlink($file);
        }
    }
    emptyDir($dir);

    if (rmdir($dir)){
        return true;
    }
}



